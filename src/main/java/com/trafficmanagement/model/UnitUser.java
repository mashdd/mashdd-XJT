package com.trafficmanagement.model;

public class UnitUser {
    private String unitAccount;      // 单位账号
    private String unitPassword;     // 单位密码

    // Getters and Setters
    public String getUnitAccount() {
        return unitAccount;
    }

    public void setUnitAccount(String unitAccount) {
        this.unitAccount = unitAccount;
    }

    public String getUnitPassword() {
        return unitPassword;
    }

    public void setUnitPassword(String unitPassword) {
        this.unitPassword = unitPassword;
    }
}
